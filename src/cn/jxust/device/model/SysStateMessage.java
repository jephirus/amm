package cn.jxust.device.model;

public class SysStateMessage extends Message {

	/**
	 * @author: Laiy
	 * @description:消防设施系统，即控制器
	 * @date:2015年10月13日
	 */
	private static final long serialVersionUID = 1L;

	private int dataUnitBsm;// 数据单元标识码，控制器 =1
	private int dataCount;// 信息数据个数

	private int dataType;// 系统类型标志码， = 1
	private String sysAddress;// 系统地址
	private String controllerStatus;// 控制器状态

	public int getDataUnitBsm() {
		return dataUnitBsm;
	}

	public void setDataUnitBsm(int dataUnitBsm) {
		this.dataUnitBsm = dataUnitBsm;
	}

	public String getControllerStatus() {
		return controllerStatus;
	}

	public void setControllerStatus(String controllerStatus) {
		this.controllerStatus = controllerStatus;
	}

	public String getSysAddress() {
		return sysAddress;
	}

	public void setSysAddress(String sysAddress) {
		this.sysAddress = sysAddress;
	}

	public int getDataType() {
		return dataType;
	}

	public void setDataType(int dataType) {
		this.dataType = dataType;
	}

	public int getDataCount() {
		return dataCount;
	}

	public void setDataCount(int dataCount) {
		this.dataCount = dataCount;
	}
}
